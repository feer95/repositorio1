function sum(x, y, z) {
    let resultado
    resultado = x + y + z
    return resultado
}
function res(x, y) {
    let resultado
    resultado = x - y
    return resultado
}
function div(x, y) {
    let resultado
    resultado = x / y
    return resultado
}
function mul(x, y) {
    let resultado
    resultado = x * y
    return resultado
}
function cuadrado(x) {
    let resultado
    resultado = x * x
    return resultado
}